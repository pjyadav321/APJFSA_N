public class Q3 {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        a += b; // a = a + b
        System.out.println("After a += b, a = " + a);
        
        a -= 2; // a = a - 2
        System.out.println("After a -= 2, a = " + a);
        
        a *= 2; // a = a * 2
        System.out.println("After a *= 2, a = " + a);
        
        a /= 2; // a = a / 2
        System.out.println("After a /= 2, a = " + a);
        
        a %= 3; // a = a % 3
        System.out.println("After a %= 3, a = " + a);
    }
}
